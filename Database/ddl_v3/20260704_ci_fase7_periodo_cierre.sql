-- Devuelve el period_id del período siguiente (creado o ya existente).
CREATE OR REPLACE FUNCTION public.sp_con_periodo_cerrar(
    p_company_id bigint,
    p_period_id bigint,
    p_usuario text
) RETURNS bigint
LANGUAGE plpgsql
AS $function$
DECLARE
    v_periodo record;
    v_fallas text;
    v_next_start date;
    v_next_end date;
    v_next_code varchar(20);
    v_next_id bigint;
    v_meses text[] := ARRAY['Enero','Febrero','Marzo','Abril','Mayo','Junio',
                            'Julio','Agosto','Septiembre','Octubre','Noviembre','Diciembre'];
BEGIN
    SELECT pp.* INTO v_periodo
    FROM public.con_periodo_contable pp
    WHERE pp.company_id = p_company_id AND pp.period_id = p_period_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'No existe el período contable % para company_id=%.',
            p_period_id, p_company_id;
    END IF;

    IF COALESCE(v_periodo.status_id, 2) <> 1 THEN
        RAISE EXCEPTION 'ESTADO_INVALIDO: solo un período en PRECIERRE puede cerrarse (período %, estado %). Ejecute primero el precierre.',
            v_periodo.code, COALESCE(v_periodo.status_id, 2);
    END IF;

    SELECT string_agg(format('%s (%s)', c.item, c.cantidad), '; ')
    INTO v_fallas
    FROM public.fn_con_checklist_cierre_periodo(p_company_id, p_period_id) c
    WHERE NOT c.ok;

    IF v_fallas IS NOT NULL THEN
        RAISE EXCEPTION 'CHECKLIST_CIERRE_PERIODO: %', v_fallas;
    END IF;

    UPDATE public.con_periodo_contable pp
    SET status_id = 2,
        status = 'CERRADO',
        closed_at = now(),
        closed_by = left(p_usuario, 100),
        updated_at = now(),
        updated_by = left(p_usuario, 100)
    WHERE pp.company_id = p_company_id AND pp.period_id = p_period_id;

    -- Crear el período siguiente si no existe
    v_next_start := (date_trunc('month', v_periodo.start_date) + interval '1 month')::date;
    v_next_end := (v_next_start + interval '1 month' - interval '1 day')::date;
    v_next_code := to_char(v_next_start, 'YYYYMM');

    SELECT pp.period_id INTO v_next_id
    FROM public.con_periodo_contable pp
    WHERE pp.company_id = p_company_id AND pp.code = v_next_code
    LIMIT 1;

    IF v_next_id IS NULL THEN
        INSERT INTO public.con_periodo_contable
            (company_id, code, name, start_date, end_date, status, status_id, created_at, created_by)
        VALUES
            (p_company_id, v_next_code,
             v_meses[extract(month FROM v_next_start)::int] || ' ' || extract(year FROM v_next_start)::int,
             v_next_start, v_next_end, 'OPEN', 0, now(), left(p_usuario, 100))
        RETURNING period_id INTO v_next_id;
    END IF;

    RETURN v_next_id;
END;
$function$;

-- Reabre un período en PRECIERRE (única reapertura permitida; un CERRADO no
