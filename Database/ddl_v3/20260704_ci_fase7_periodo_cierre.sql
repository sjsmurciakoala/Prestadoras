-- 6. Repunte de sp_informacion_ciclo (GetCiclo legacy del WS de lectores)
--    Misma firma y semántica (ruta → ciclo → último período abierto del ciclo).
--    Sin parámetro de empresa: el WS es company-agnóstico y NO cambia (D9);
--    igual que el legacy, resuelve sobre todos los tenants (rutas/ciclos son
--    catálogos globales). El WS actual usa un query inline sobre historialmes
--    (cubierto por el espejo de la sección 3); este SP queda como fallback.
-- ----------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.sp_informacion_ciclo(p_ruta character varying)
 RETURNS TABLE(r_ciclo integer, r_ano integer, r_mes integer)
 LANGUAGE plpgsql
AS $function$
DECLARE
    v_ciclo integer;
    v_ruta_original text;
    v_ruta_normalizada text;
BEGIN
    v_ruta_original := btrim(coalesce(p_ruta, ''));
    v_ruta_normalizada := v_ruta_original;

    IF v_ruta_original ~ '^[0-9]+$' THEN
        v_ruta_normalizada := lpad(v_ruta_original, 5, '0');
    END IF;

    SELECT r.codciclo
    INTO v_ciclo
    FROM rutas r
    WHERE r.codruta = v_ruta_normalizada
       OR r.codruta = v_ruta_original
    ORDER BY CASE WHEN r.codruta = v_ruta_normalizada THEN 0 ELSE 1 END
    LIMIT 1;

    IF v_ciclo IS NULL THEN
        RETURN;
    END IF;

    -- F7: el período comercial vive en adm_periodo_comercial(_ciclo)
    RETURN QUERY
    SELECT
        CASE
            WHEN pc.ciclo_codigo ~ '^[0-9]+$' THEN pc.ciclo_codigo::int
            ELSE v_ciclo
        END AS r_ciclo,
        p.anio AS r_ano,
        p.mes::int AS r_mes
    FROM public.adm_periodo_comercial_ciclo pc
    JOIN public.adm_periodo_comercial p
      ON p.company_id = pc.company_id
     AND p.periodo_comercial_id = pc.periodo_comercial_id
    WHERE p.status_id = 1
      AND pc.status_id = 1
      AND (pc.ciclo_codigo = lpad(v_ciclo::text, 2, '0') OR pc.ciclo_codigo = v_ciclo::text)
    ORDER BY p.anio DESC, p.mes DESC
    LIMIT 1;
END
$function$;

-- ----------------------------------------------------------------------------
-- 7. Cierre comercial: rutas pendientes, cierre de ciclo, checklist y cierre
--    de mes, apertura habilitada por el cierre del mes anterior.
-- ----------------------------------------------------------------------------

-- Rutas del ciclo con su avance de facturación del mes. "Pendiente" = ruta
-- activa con clientes activos y CERO facturas de lectura emitidas en el mes
-- (validación contra factura, NO contra historicomedicion: las filas de
-- historicomedicion se siembran al abrir el período y no prueban sincronía).
-- Rutas activas sin clientes asignados no bloquean (no aparecen).
CREATE OR REPLACE FUNCTION public.fn_adm_periodo_ciclo_rutas_pendientes(
    p_company_id bigint,
    p_periodo_ciclo_id bigint
) RETURNS TABLE(
    codruta varchar,
    clientes_activos bigint,
    facturas_mes bigint,
    pendiente boolean
)
LANGUAGE plpgsql
STABLE
AS $function$
DECLARE
    v_ciclo record;
    v_ciclo_id integer;
BEGIN
    SELECT pc.ciclo_codigo, p.anio, p.mes
    INTO v_ciclo
    FROM public.adm_periodo_comercial_ciclo pc
    JOIN public.adm_periodo_comercial p
      ON p.company_id = pc.company_id
     AND p.periodo_comercial_id = pc.periodo_comercial_id
    WHERE pc.company_id = p_company_id
      AND pc.periodo_ciclo_id = p_periodo_ciclo_id;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'No existe el ciclo de período comercial % para company_id=%.',
            p_periodo_ciclo_id, p_company_id;
    END IF;

    SELECT c.ciclos_id
    INTO v_ciclo_id
    FROM public.ciclos c
    WHERE btrim(c.ciclos_codigo) = v_ciclo.ciclo_codigo
       OR (c.ciclos_codigo ~ '^[0-9]+$' AND v_ciclo.ciclo_codigo ~ '^[0-9]+$'
           AND c.ciclos_codigo::int = v_ciclo.ciclo_codigo::int)
    ORDER BY CASE WHEN btrim(c.ciclos_codigo) = v_ciclo.ciclo_codigo THEN 0 ELSE 1 END
    LIMIT 1;

    IF v_ciclo_id IS NULL THEN
        -- ciclo sin fila de catálogo: no hay rutas que validar
        RETURN;
    END IF;

    RETURN QUERY
    SELECT r.codruta::varchar,
           count(DISTINCT cm.maestro_cliente_id) AS clientes_activos,
           count(DISTINCT f.id) AS facturas_mes,
           (count(DISTINCT f.id) = 0) AS pendiente
    FROM public.rutas r
    JOIN public.cliente_maestro cm
      ON cm.company_id = p_company_id
     AND cm.estado = true
     AND cm.ciclos_id = v_ciclo_id
     AND btrim(split_part(COALESCE(cm.maestro_cliente_indicativo_ruta, ''), '-', 3)) = btrim(r.codruta)
    LEFT JOIN public.factura f
      ON f.company_id = p_company_id
     AND btrim(f.clientecodigo) = btrim(cm.maestro_cliente_clave)
     AND f.tipofacturacion = 'S'
     AND f.tipofactura = 'F'
     AND COALESCE(f.estado_id, 1) <> 3
     AND COALESCE(f.estado, 'A') <> 'N'
     AND btrim(f.ano) ~ '^[0-9]+$' AND btrim(f.ano)::int = v_ciclo.anio
     AND btrim(f.mes) ~ '^[0-9]+$' AND btrim(f.mes)::int = v_ciclo.mes
    WHERE r.estado = true
      AND r.codciclo = v_ciclo_id
    GROUP BY r.codruta
    ORDER BY r.codruta;
END;
$function$;

-- Cierra un ciclo del período comercial. p_forzar=true omite la validación de
-- rutas pendientes (lo usa el flujo legacy de "Generar período" del auxiliar
-- de lectura, que siempre cerró sin validar; el cierre formal de la pantalla
-- de períodos valida u ofrece forzar con confirmación explícita).
CREATE OR REPLACE FUNCTION public.sp_adm_periodo_ciclo_cerrar(
    p_company_id bigint,
    p_periodo_ciclo_id bigint,
    p_usuario text,
    p_forzar boolean DEFAULT false
) RETURNS void
LANGUAGE plpgsql
AS $function$
DECLARE
    v_ciclo record;
    v_pendientes bigint;
BEGIN
    SELECT pc.*, p.anio, p.mes
    INTO v_ciclo
    FROM public.adm_periodo_comercial_ciclo pc
    JOIN public.adm_periodo_comercial p
      ON p.company_id = pc.company_id
     AND p.periodo_comercial_id = pc.periodo_comercial_id
    WHERE pc.company_id = p_company_id
      AND pc.periodo_ciclo_id = p_periodo_ciclo_id
    FOR UPDATE OF pc;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'No existe el ciclo de período comercial % para company_id=%.',
            p_periodo_ciclo_id, p_company_id;
    END IF;

    IF v_ciclo.status_id <> 1 THEN
        RAISE EXCEPTION 'CICLO_YA_CERRADO: el ciclo % del período %-% ya está cerrado.',
            v_ciclo.ciclo_codigo, v_ciclo.anio, lpad(v_ciclo.mes::text, 2, '0');
    END IF;

    IF NOT p_forzar THEN
        SELECT count(*)
        INTO v_pendientes
        FROM public.fn_adm_periodo_ciclo_rutas_pendientes(p_company_id, p_periodo_ciclo_id) rp
        WHERE rp.pendiente;

        IF v_pendientes > 0 THEN
            RAISE EXCEPTION 'CICLO_RUTAS_PENDIENTES: % ruta(s) del ciclo % sin facturas emitidas en %-%.',
                v_pendientes, v_ciclo.ciclo_codigo, v_ciclo.anio, lpad(v_ciclo.mes::text, 2, '0');
        END IF;
    END IF;

    UPDATE public.adm_periodo_comercial_ciclo pc
    SET status_id = 2,
        fecha_cierre = now(),
        cerrado_por = left(p_usuario, 100),
        updated_at = now(),
        updated_by = left(p_usuario, 100)
    WHERE pc.company_id = p_company_id
      AND pc.periodo_ciclo_id = p_periodo_ciclo_id;
END;
$function$;

-- Checklist del cierre del mes comercial.
CREATE OR REPLACE FUNCTION public.fn_adm_periodo_comercial_checklist(
    p_company_id bigint,
    p_periodo_comercial_id bigint
) RETURNS TABLE(
    item varchar,
    ok boolean,
    cantidad bigint,
    detalle text
)
LANGUAGE plpgsql
STABLE
AS $function$
DECLARE
    v_periodo record;
    v_n bigint;
    v_detalle text;
BEGIN
    SELECT p.*
    INTO v_periodo
    FROM public.adm_periodo_comercial p
    WHERE p.company_id = p_company_id
      AND p.periodo_comercial_id = p_periodo_comercial_id;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'No existe el período comercial % para company_id=%.',
            p_periodo_comercial_id, p_company_id;
    END IF;

    -- 1. Todos los ciclos del mes cerrados
    SELECT count(*), string_agg(pc.ciclo_codigo, ', ' ORDER BY pc.ciclo_codigo)
    INTO v_n, v_detalle
    FROM public.adm_periodo_comercial_ciclo pc
    WHERE pc.company_id = p_company_id
      AND pc.periodo_comercial_id = p_periodo_comercial_id
      AND pc.status_id = 1;

    RETURN QUERY SELECT
        'CICLOS_ABIERTOS'::varchar,
        v_n = 0,
        v_n,
        CASE WHEN v_n = 0 THEN 'Todos los ciclos del mes están cerrados.'
             ELSE format('Ciclos abiertos: %s.', v_detalle) END;

    -- 2. Sin rutas pendientes en los ciclos abiertos (validado contra facturas
    --    emitidas; los ciclos ya cerrados validaron sus rutas al cerrarse)
    SELECT COALESCE(sum(pend.pendientes), 0)
    INTO v_n
    FROM public.adm_periodo_comercial_ciclo pc
    CROSS JOIN LATERAL (
        SELECT count(*) AS pendientes
        FROM public.fn_adm_periodo_ciclo_rutas_pendientes(p_company_id, pc.periodo_ciclo_id) rp
        WHERE rp.pendiente
    ) pend
    WHERE pc.company_id = p_company_id
      AND pc.periodo_comercial_id = p_periodo_comercial_id
      AND pc.status_id = 1;

    RETURN QUERY SELECT
        'RUTAS_PENDIENTES'::varchar,
        v_n = 0,
        v_n,
        CASE WHEN v_n = 0 THEN 'Sin rutas pendientes de facturar.'
             ELSE format('%s ruta(s) sin facturas emitidas en el mes.', v_n) END;
END;
$function$;

-- Cierra el mes comercial (requiere checklist en verde). Habilita abrir el
-- mes siguiente (sp_adm_periodo_comercial_abrir valida la secuencia).
CREATE OR REPLACE FUNCTION public.sp_adm_periodo_comercial_cerrar(
    p_company_id bigint,
    p_periodo_comercial_id bigint,
    p_usuario text
) RETURNS void
LANGUAGE plpgsql
AS $function$
DECLARE
    v_periodo record;
    v_fallas text;
BEGIN
    SELECT p.*
    INTO v_periodo
    FROM public.adm_periodo_comercial p
    WHERE p.company_id = p_company_id
      AND p.periodo_comercial_id = p_periodo_comercial_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'No existe el período comercial % para company_id=%.',
            p_periodo_comercial_id, p_company_id;
    END IF;

    IF v_periodo.status_id <> 1 THEN
        RAISE EXCEPTION 'PERIODO_YA_CERRADO: el período comercial %-% ya está cerrado.',
            v_periodo.anio, lpad(v_periodo.mes::text, 2, '0');
    END IF;

    SELECT string_agg(format('%s (%s)', c.item, c.cantidad), '; ')
    INTO v_fallas
    FROM public.fn_adm_periodo_comercial_checklist(p_company_id, p_periodo_comercial_id) c
    WHERE NOT c.ok;

    IF v_fallas IS NOT NULL THEN
        RAISE EXCEPTION 'CHECKLIST_CIERRE_COMERCIAL: %', v_fallas;
    END IF;

    UPDATE public.adm_periodo_comercial p
    SET status_id = 2,
        fecha_cierre = now(),
        cerrado_por = left(p_usuario, 100),
        updated_at = now(),
        updated_by = left(p_usuario, 100)
    WHERE p.company_id = p_company_id
      AND p.periodo_comercial_id = p_periodo_comercial_id;
END;
$function$;

-- Abre (o reutiliza) el período comercial del mes y agrega el ciclo indicado.
-- p_validar_secuencia=true exige que el período del mes calendario anterior,
-- si existe, esté cerrado ("el cierre habilita el mes siguiente", D7). El
-- flujo legacy del auxiliar de lectura llama con false (conserva su
-- comportamiento histórico de generar sin validar secuencia).
-- Devuelve periodo_comercial_id.
CREATE OR REPLACE FUNCTION public.sp_adm_periodo_comercial_abrir(
    p_company_id bigint,
    p_anio integer,
    p_mes integer,
    p_ciclo character varying,
    p_usuario text,
    p_validar_secuencia boolean DEFAULT true
) RETURNS bigint
LANGUAGE plpgsql
AS $function$
DECLARE
    v_ciclo_norm text;
    v_periodo record;
    v_periodo_id bigint;
    v_ciclo_row record;
    v_anio_prev integer;
    v_mes_prev integer;
BEGIN
    IF p_anio IS NULL OR p_mes IS NULL OR p_mes NOT BETWEEN 1 AND 12 THEN
        RAISE EXCEPTION 'Período comercial inválido: anio=%, mes=%.', p_anio, p_mes;
    END IF;

    v_ciclo_norm := btrim(coalesce(p_ciclo, ''));
    IF v_ciclo_norm = '' THEN
        v_ciclo_norm := '01';
    ELSIF v_ciclo_norm ~ '^[0-9]+$' THEN
        v_ciclo_norm := lpad(v_ciclo_norm, 2, '0');
    END IF;

    IF length(v_ciclo_norm) > 2 THEN
        RAISE EXCEPTION 'Código de ciclo inválido: "%". Se esperan códigos cortos (01, 1).', p_ciclo;
    END IF;

    IF p_validar_secuencia THEN
        IF p_mes = 1 THEN
            v_anio_prev := p_anio - 1; v_mes_prev := 12;
        ELSE
            v_anio_prev := p_anio; v_mes_prev := p_mes - 1;
        END IF;

        IF EXISTS (
            SELECT 1 FROM public.adm_periodo_comercial p
            WHERE p.company_id = p_company_id
              AND p.anio = v_anio_prev AND p.mes = v_mes_prev
              AND p.status_id = 1
        ) THEN
            RAISE EXCEPTION 'PERIODO_ANTERIOR_ABIERTO: cierre el mes comercial %-% antes de abrir %-%.',
                v_anio_prev, lpad(v_mes_prev::text, 2, '0'), p_anio, lpad(p_mes::text, 2, '0');
        END IF;
    END IF;

    SELECT p.* INTO v_periodo
    FROM public.adm_periodo_comercial p
    WHERE p.company_id = p_company_id AND p.anio = p_anio AND p.mes = p_mes
    FOR UPDATE;

    IF FOUND THEN
        IF v_periodo.status_id <> 1 THEN
            RAISE EXCEPTION 'PERIODO_CERRADO: el período comercial %-% ya fue cerrado; no se reabre.',
                p_anio, lpad(p_mes::text, 2, '0');
        END IF;
        v_periodo_id := v_periodo.periodo_comercial_id;
    ELSE
        INSERT INTO public.adm_periodo_comercial
            (company_id, anio, mes, status_id, fecha_apertura, abierto_por, created_by)
        VALUES
            (p_company_id, p_anio, p_mes, 1, now(), left(p_usuario, 100), left(p_usuario, 100))
        RETURNING periodo_comercial_id INTO v_periodo_id;
    END IF;

    SELECT pc.* INTO v_ciclo_row
    FROM public.adm_periodo_comercial_ciclo pc
    WHERE pc.company_id = p_company_id
      AND pc.periodo_comercial_id = v_periodo_id
      AND pc.ciclo_codigo = v_ciclo_norm;

    IF FOUND THEN
        IF v_ciclo_row.status_id <> 1 THEN
            RAISE EXCEPTION 'CICLO_CERRADO: el ciclo % del período %-% ya fue cerrado; no se reabre.',
                v_ciclo_norm, p_anio, lpad(p_mes::text, 2, '0');
        END IF;
        -- ya abierto: idempotente
    ELSE
        INSERT INTO public.adm_periodo_comercial_ciclo
            (company_id, periodo_comercial_id, ciclo_codigo, status_id,
             fecha_apertura, abierto_por, fecha_limite, created_by)
        VALUES
            (p_company_id, v_periodo_id, v_ciclo_norm, 1,
             now(), left(p_usuario, 100),
             (make_date(p_anio, p_mes, 1) + interval '1 month' - interval '1 day')::date,
             left(p_usuario, 100));
    END IF;

    RETURN v_periodo_id;
END;
$function$;

