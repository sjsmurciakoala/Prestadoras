-- ----------------------------------------------------------------------------
-- 10. Avisos de períodos para el banner del portal.
-- ----------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.fn_adm_avisos_periodos(
    p_company_id bigint
) RETURNS TABLE(
    tipo varchar,
    severidad varchar,
    mensaje text,
    cantidad bigint
)
LANGUAGE plpgsql
STABLE
AS $function$
DECLARE
    v_hoy_anio integer := extract(year FROM current_date)::int;
    v_hoy_mes integer := extract(month FROM current_date)::int;
    v_hoy_ym integer;
    v_desfase_max integer := 1;
    v_activo_fact boolean := false;
    v_com_ym integer;
    v_con_ym integer;
    v_n bigint;
    v_detalle text;
BEGIN
    v_hoy_ym := v_hoy_anio * 12 + v_hoy_mes;

    SELECT COALESCE(c.desfase_max_meses, 1), COALESCE(c.activo_facturacion, false)
    INTO v_desfase_max, v_activo_fact
    FROM public.con_integracion_config c
    WHERE c.company_id = p_company_id;

    -- 1. Mes comercial vencido sin cerrar
    SELECT count(*), string_agg(format('%s-%s', p.anio, lpad(p.mes::text, 2, '0')), ', ' ORDER BY p.anio, p.mes)
    INTO v_n, v_detalle
    FROM public.adm_periodo_comercial p
    WHERE p.company_id = p_company_id
      AND p.status_id = 1
      AND (p.anio * 12 + p.mes) < v_hoy_ym;

    IF v_n > 0 THEN
        RETURN QUERY SELECT 'COMERCIAL_VENCIDO'::varchar, 'WARN'::varchar,
            format('Mes comercial vencido sin cerrar: %s.', v_detalle), v_n;
    END IF;

    -- 2. Período comercial del mes actual inexistente
    IF NOT EXISTS (
        SELECT 1 FROM public.adm_periodo_comercial p
        WHERE p.company_id = p_company_id AND p.anio = v_hoy_anio AND p.mes = v_hoy_mes
    ) THEN
        RETURN QUERY SELECT 'COMERCIAL_INEXISTENTE'::varchar, 'INFO'::varchar,
            format('No existe el período comercial %s-%s.', v_hoy_anio, lpad(v_hoy_mes::text, 2, '0')), 1::bigint;
    END IF;

    -- 3. Período contable vencido sin cerrar
    SELECT count(*), string_agg(pp.code, ', ' ORDER BY pp.start_date)
    INTO v_n, v_detalle
    FROM public.con_periodo_contable pp
    WHERE pp.company_id = p_company_id
      AND COALESCE(pp.status_id, 2) = 0
      AND pp.end_date::date < date_trunc('month', current_date)::date;

    IF v_n > 0 THEN
        RETURN QUERY SELECT 'CONTABLE_VENCIDO'::varchar, 'WARN'::varchar,
            format('Período contable vencido sin cerrar: %s.', v_detalle), v_n;
    END IF;

    -- 4. Período contable del mes actual inexistente
    IF NOT EXISTS (
        SELECT 1 FROM public.con_periodo_contable pp
        WHERE pp.company_id = p_company_id
          AND current_date BETWEEN pp.start_date::date AND pp.end_date::date
    ) THEN
        RETURN QUERY SELECT 'CONTABLE_INEXISTENTE'::varchar, 'WARN'::varchar,
            'No existe un período contable que cubra la fecha actual.'::text, 1::bigint;
    END IF;

    -- 5. Facturas sin partida (total acumulado, si la integración está activa)
    IF v_activo_fact THEN
        SELECT count(*) INTO v_n
        FROM public.factura f
        WHERE f.company_id = p_company_id
          AND f.tipofacturacion = 'S'
          AND f.tipofactura = 'F'
          AND COALESCE(f.estado_id, 1) <> 3
          AND COALESCE(f.estado, 'A') <> 'N'
          AND NOT EXISTS (
              SELECT 1 FROM public.con_partida_factura pf
              WHERE pf.company_id = p_company_id AND pf.factura_id = f.id);

        IF v_n > 0 THEN
            RETURN QUERY SELECT 'FACTURAS_SIN_PARTIDA'::varchar, 'INFO'::varchar,
                format('%s factura(s) sin partida contable (generar lote de facturación).', v_n), v_n;
        END IF;
    END IF;

    -- 6. Cola de partidas pendientes
    SELECT count(*) INTO v_n
    FROM public.con_partida_pendiente pp
    WHERE pp.company_id = p_company_id AND pp.status_id = 1;

    IF v_n > 0 THEN
        RETURN QUERY SELECT 'PENDIENTES_COLA'::varchar, 'INFO'::varchar,
            format('%s partida(s) pendiente(s) en cola de regularización.', v_n), v_n;
    END IF;

    -- 7. Desfase comercial-contable mayor al configurado
    SELECT max(p.anio * 12 + p.mes) INTO v_com_ym
    FROM public.adm_periodo_comercial p
    WHERE p.company_id = p_company_id AND p.status_id = 1;

    SELECT max(extract(year FROM pp.start_date)::int * 12 + extract(month FROM pp.start_date)::int)
    INTO v_con_ym
    FROM public.con_periodo_contable pp
    WHERE pp.company_id = p_company_id AND COALESCE(pp.status_id, 2) = 0;

    IF v_com_ym IS NOT NULL AND v_con_ym IS NOT NULL
       AND abs(v_com_ym - v_con_ym) > v_desfase_max THEN
        RETURN QUERY SELECT 'DESFASE_COMERCIAL_CONTABLE'::varchar, 'WARN'::varchar,
            format('El mes comercial abierto y el período contable abierto difieren %s mes(es) (máximo configurado: %s).',
                   abs(v_com_ym - v_con_ym), v_desfase_max), abs(v_com_ym - v_con_ym)::bigint;
    END IF;
END;
$function$;

COMMIT;
