-- ----------------------------------------------------------------------------
-- 9. Cierre del mes contable: checklist → precierre → cierre → crea siguiente.
--    Estados de con_periodo_contable (convención existente): 0=ABIERTO,
--    1=PRECIERRE, 2=CERRADO. Desde F6, cerrado ⇒ los reportes leen el caché
--    con_saldo_cuenta; por eso el checklist incluye la verificación de saldos.
-- ----------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.fn_con_checklist_cierre_periodo(
    p_company_id bigint,
    p_period_id bigint
) RETURNS TABLE(
    item varchar,
    ok boolean,
    cantidad numeric,
    detalle text
)
LANGUAGE plpgsql
STABLE
AS $function$
DECLARE
    v_periodo record;
    v_desde date;
    v_hasta date;
    v_activo_fact boolean := false;
    v_activo_caja boolean := false;
    v_n numeric;
BEGIN
    SELECT pp.* INTO v_periodo
    FROM public.con_periodo_contable pp
    WHERE pp.company_id = p_company_id AND pp.period_id = p_period_id;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'No existe el período contable % para company_id=%.',
            p_period_id, p_company_id;
    END IF;

    v_desde := v_periodo.start_date::date;
    v_hasta := v_periodo.end_date::date;

    SELECT COALESCE(c.activo_facturacion, false), COALESCE(c.activo_caja, false)
    INTO v_activo_fact, v_activo_caja
    FROM public.con_integracion_config c
    WHERE c.company_id = p_company_id;

    -- 1. Facturas del período con partida generada (puente con_partida_factura, F3)
    IF v_activo_fact THEN
        SELECT count(*) INTO v_n
        FROM public.factura f
        WHERE f.company_id = p_company_id
          AND f.tipofacturacion = 'S'
          AND f.tipofactura = 'F'
          AND COALESCE(f.estado_id, 1) <> 3
          AND COALESCE(f.estado, 'A') <> 'N'
          AND f.fechaemision BETWEEN v_desde AND v_hasta
          AND NOT EXISTS (
              SELECT 1 FROM public.con_partida_factura pf
              WHERE pf.company_id = p_company_id AND pf.factura_id = f.id);

        RETURN QUERY SELECT
            'FACTURAS_SIN_PARTIDA'::varchar, v_n = 0, v_n,
            CASE WHEN v_n = 0 THEN 'Todas las facturas del período tienen partida.'
                 ELSE format('%s factura(s) sin partida: generar el lote de partidas de facturación para el rango %s a %s.', v_n, v_desde, v_hasta) END;
    ELSE
        RETURN QUERY SELECT
            'FACTURAS_SIN_PARTIDA'::varchar, true, 0::numeric,
            'Integración de facturación no activa para la empresa.'::text;
    END IF;

    -- 2. Sin partidas en borrador dentro del período
    SELECT count(*) INTO v_n
    FROM public.con_partida_hdr h
    WHERE h.company_id = p_company_id
      AND h.status = 0
      AND (h.period_id = p_period_id
           OR (h.period_id IS NULL AND h.poliza_date::date BETWEEN v_desde AND v_hasta));

    RETURN QUERY SELECT
        'PARTIDAS_BORRADOR'::varchar, v_n = 0, v_n,
        CASE WHEN v_n = 0 THEN 'Sin partidas en borrador.'
             ELSE format('%s partida(s) en borrador: postearlas o eliminarlas antes de cerrar.', v_n) END;

    -- 3. Caja del período posteada (transacciones de sesión de caja 201/202
    --    con comprobante POSTED del módulo CAJA)
    IF v_activo_caja THEN
        SELECT count(*) INTO v_n
        FROM public.transaccion_abonado t
        WHERE t.company_id = p_company_id
          AND t.caja_id IS NOT NULL
          AND btrim(COALESCE(t.tipotransaccion, '')) IN ('201', '202')
          AND COALESCE(t.estado_id, 1) <> 3
          AND t.fecha_registro BETWEEN v_desde AND v_hasta
          AND NOT EXISTS (
              SELECT 1 FROM public.con_partida_hdr h
              WHERE h.company_id = p_company_id
                AND h.module = 'CAJA'
                AND h.document_id = t.ide
                AND h.status = 1);

        RETURN QUERY SELECT
            'CAJA_SIN_POSTEAR'::varchar, v_n = 0, v_n,
            CASE WHEN v_n = 0 THEN 'La caja del período está posteada.'
                 ELSE format('%s transacción(es) de caja sin comprobante contable posteado.', v_n) END;
    ELSE
        RETURN QUERY SELECT
            'CAJA_SIN_POSTEAR'::varchar, true, 0::numeric,
            'Integración de caja no activa para la empresa.'::text;
    END IF;

    -- 4. Cola de partidas pendientes vacía (toda la empresa: una pendiente de
    --    cualquier fecha se postearía en este período abierto al reprocesarla)
    SELECT count(*) INTO v_n
    FROM public.con_partida_pendiente pp
    WHERE pp.company_id = p_company_id
      AND pp.status_id = 1;

    RETURN QUERY SELECT
        'PENDIENTES_COLA'::varchar, v_n = 0, v_n,
        CASE WHEN v_n = 0 THEN 'Cola de partidas pendientes vacía.'
             ELSE format('%s partida(s) pendiente(s) en cola: reprocesarlas o descartarlas.', v_n) END;

    -- 5. Verificación de saldos del período (F6): caché vs libro sin divergencias
    SELECT count(*) INTO v_n
    FROM public.fn_con_verificar_saldo_cuenta(p_company_id, p_period_id);

    RETURN QUERY SELECT
        'SALDOS_CACHE'::varchar, v_n = 0, v_n,
        CASE WHEN v_n = 0 THEN 'Caché de saldos consistente con el libro.'
             ELSE format('%s divergencia(s) caché vs libro: revisar con "Verificar saldos" antes de cerrar.', v_n) END;
END;
$function$;

CREATE OR REPLACE FUNCTION public.sp_con_periodo_precerrar(
    p_company_id bigint,
    p_period_id bigint,
    p_usuario text
) RETURNS void
LANGUAGE plpgsql
AS $function$
