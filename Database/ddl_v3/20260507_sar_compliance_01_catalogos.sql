-- ----------------------------------------------------------------------------
-- 4) adm_establecimiento — N establecimientos por empresa (multi-empresa)
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.adm_establecimiento (
    establecimiento_id bigint GENERATED BY DEFAULT AS IDENTITY,
    company_id bigint NOT NULL,

    -- identificación SAR
    codigo varchar(20) NOT NULL,
    nombre varchar(200) NOT NULL,

    -- datos fiscales (pueden diferir de la empresa madre)
    rtn_emisor varchar(20) NOT NULL,
    razon_social_emisor varchar(200) NOT NULL,
    direccion_emisor varchar(300) NOT NULL,
    telefono varchar(50),
    correo varchar(150),

    -- estado
    status_id smallint NOT NULL DEFAULT 1,
    es_principal boolean NOT NULL DEFAULT false,

    created_at timestamptz NOT NULL DEFAULT now(),
    created_by varchar(100) NOT NULL DEFAULT current_user,
    updated_at timestamptz,
    updated_by varchar(100),

    CONSTRAINT pk_adm_establecimiento PRIMARY KEY (establecimiento_id),
    CONSTRAINT uq_adm_establecimiento_company_estab UNIQUE (company_id, establecimiento_id),
    CONSTRAINT fk_adm_establecimiento_company FOREIGN KEY (company_id) REFERENCES public.cfg_company (company_id) ON DELETE CASCADE,
    CONSTRAINT uq_adm_establecimiento_codigo UNIQUE (company_id, codigo),
    CONSTRAINT ck_adm_establecimiento_rtn_format CHECK (rtn_emisor ~ '^[0-9]{14}$'),
    CONSTRAINT ck_adm_establecimiento_status CHECK (status_id IN (0, 1))
);

CREATE INDEX IF NOT EXISTS ix_adm_establecimiento_company
    ON public.adm_establecimiento (company_id, status_id);

-- Una sola empresa puede tener un solo principal activo
CREATE UNIQUE INDEX IF NOT EXISTS uq_adm_establecimiento_principal_por_company
    ON public.adm_establecimiento (company_id)
    WHERE es_principal = true AND status_id = 1;

-- ----------------------------------------------------------------------------
-- 5) Seed inicial: para cada cfg_company que aún no tenga establecimiento
--    principal, crear uno copiando los datos de la empresa.
--    Idempotente: solo inserta si no existe.
-- ----------------------------------------------------------------------------
INSERT INTO public.adm_establecimiento
    (company_id, codigo, nombre, rtn_emisor, razon_social_emisor, direccion_emisor, telefono, correo, es_principal, status_id)
SELECT
    c.company_id,
    'PRINCIPAL'                              AS codigo,
    COALESCE(c.commercial_name, c.legal_name) AS nombre,
    c.tax_id                                 AS rtn_emisor,
    c.legal_name                             AS razon_social_emisor,
    COALESCE(c.address, 'PENDIENTE')         AS direccion_emisor,
    c.phone                                  AS telefono,
    c.email                                  AS correo,
    true                                     AS es_principal,
    1                                        AS status_id
FROM public.cfg_company c
WHERE NOT EXISTS (
    SELECT 1
    FROM public.adm_establecimiento e
    WHERE e.company_id = c.company_id
      AND e.es_principal = true
      AND e.status_id = 1
)
  AND c.tax_id ~ '^[0-9]{14}$';   -- solo seed si la empresa tiene RTN válido

COMMIT;

