SELECT c.company_id, c.code, c.legal_name, c.tax_id
FROM public.cfg_company c
LEFT JOIN public.adm_establecimiento e
       ON e.company_id = c.company_id AND e.es_principal = true AND e.status_id = 1
WHERE e.establecimiento_id IS NULL;
