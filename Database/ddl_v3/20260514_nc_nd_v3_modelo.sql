'Notas de Crédito V3 (SAR Acuerdo 481-2017). Disminuyen el saldo del cliente / anulan factura. Modelo mono-sucursal: usa establecimiento_codigo varchar(3), emisor snapshot desde cfg_company.';
COMMENT ON TABLE public.adm_nota_debito IS
'Notas de Débito V3 (SAR Acuerdo 481-2017). Aumentan el saldo del cliente (cobro extemporáneo, mora, ajuste). No anulan la factura origen.';
