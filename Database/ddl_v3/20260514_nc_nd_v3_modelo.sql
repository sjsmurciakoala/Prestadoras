-- -----------------------------------------------------------------------------
-- 2.1 adm_nota_credito
-- -----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.adm_nota_credito (
    nota_credito_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id bigint NOT NULL,
    establecimiento_codigo varchar(3) NOT NULL DEFAULT '000',

    -- documento fiscal
    tipo_documento_fiscal_id smallint NOT NULL DEFAULT 6,  -- NC
    numero_documento varchar(80) NOT NULL,
    cai_id bigint NOT NULL,                  -- CAI específico tipo NC
    correlativo bigint NOT NULL,
    fecha_emision timestamptz NOT NULL DEFAULT now(),
    fecha_limite_cai date NOT NULL,
    leyenda_cai_rango varchar(200),

    -- emisor (snapshot de cfg_company al momento de emitir)
    rtn_emisor varchar(20) NOT NULL,
    razon_social_emisor varchar(200) NOT NULL,
    direccion_emisor varchar(300),

    -- receptor (cliente de la factura origen)
    cliente_id bigint NOT NULL,
    rtn_receptor varchar(20),
    razon_social_receptor varchar(200) NOT NULL,
    direccion_receptor varchar(300),

    -- referencia al documento original (OBLIGATORIO SAR)
    factura_origen_id integer NOT NULL,
    factura_origen_numero varchar(80) NOT NULL,
    factura_origen_fecha date NOT NULL,
    factura_origen_cai varchar(100),

    -- motivo
    motivo_anulacion_id smallint NOT NULL,
    motivo_detalle varchar(500),

    -- valores
    monto_disminuir numeric(18,4) NOT NULL,
    isv_disminuir numeric(18,4) NOT NULL DEFAULT 0,
    total_nota numeric(18,4) NOT NULL,
    anula_factura_origen boolean NOT NULL DEFAULT false,  -- true si la NC cubre el total

    -- estado (cfg_estado_documento_fiscal: 1=EMITIDA 2=APLICADA 3=ANULADA 4=PENDIENTE)
    estado_id smallint NOT NULL DEFAULT 1,

    -- auditoría
    usuario_emisor varchar(100) NOT NULL,
    created_at timestamptz NOT NULL DEFAULT now(),
    created_by varchar(100) NOT NULL DEFAULT current_user,
    updated_at timestamptz,
    updated_by varchar(100),

    CONSTRAINT fk_adm_nota_credito_company
        FOREIGN KEY (company_id) REFERENCES public.cfg_company (company_id) ON DELETE CASCADE,
    CONSTRAINT fk_adm_nota_credito_cai
        FOREIGN KEY (company_id, cai_id) REFERENCES public.adm_cai_facturacion (company_id, cai_id),
    CONSTRAINT fk_adm_nota_credito_motivo
        FOREIGN KEY (motivo_anulacion_id) REFERENCES public.cfg_motivo_anulacion (motivo_anulacion_id),
    CONSTRAINT fk_adm_nota_credito_tipo_doc
        FOREIGN KEY (tipo_documento_fiscal_id) REFERENCES public.cfg_tipo_documento_fiscal (tipo_documento_fiscal_id),
    CONSTRAINT fk_adm_nota_credito_estado
        FOREIGN KEY (estado_id) REFERENCES public.cfg_estado_documento_fiscal (estado_id),
    CONSTRAINT fk_adm_nota_credito_factura_origen
        FOREIGN KEY (factura_origen_id) REFERENCES public.factura (id),
    CONSTRAINT fk_adm_nota_credito_cliente
        FOREIGN KEY (cliente_id) REFERENCES public.cliente_maestro (maestro_cliente_id),
    CONSTRAINT ck_adm_nota_credito_montos
        CHECK (monto_disminuir >= 0 AND total_nota >= 0 AND isv_disminuir >= 0),
    CONSTRAINT uq_adm_nota_credito_company_cai_correlativo
        UNIQUE (company_id, cai_id, correlativo)
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_adm_nota_credito_company_numero
    ON public.adm_nota_credito (company_id, numero_documento);

CREATE INDEX IF NOT EXISTS ix_adm_nota_credito_factura_origen
    ON public.adm_nota_credito (company_id, factura_origen_id);

