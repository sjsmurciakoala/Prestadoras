-- -----------------------------------------------------------------------------
-- 1. cfg_motivo_aumento — catálogo de motivos para Nota de Débito
-- -----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.cfg_motivo_aumento (
    motivo_aumento_id smallint PRIMARY KEY,
    codigo varchar(20) NOT NULL UNIQUE,
    descripcion varchar(150) NOT NULL,
    activo boolean NOT NULL DEFAULT true
);

INSERT INTO public.cfg_motivo_aumento (motivo_aumento_id, codigo, descripcion) VALUES
    (1, 'COBRO_EXTEMPORANEO', 'Cobro de servicio no facturado en su período'),
    (2, 'RECARGO_MORA',       'Recargo por mora sobre saldo vencido'),
